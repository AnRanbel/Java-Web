package com.wy.user;

public class ProductInfo {
	
	private String id; 
	private String product_type;
	private String product_brand;
	private String product_name;
	private String old_price;
	private String new_price;
	private String product_info;
	private String product_img;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the product_type
	 */
	public String getProduct_type() {
		return product_type;
	}
	/**
	 * @param product_type the product_type to set
	 */
	public void setProduct_type(String product_type) {
		this.product_type = product_type;
	}
	/**
	 * @return the product_brand
	 */
	public String getProduct_brand() {
		return product_brand;
	}
	/**
	 * @param product_brand the product_brand to set
	 */
	public void setProduct_brand(String product_brand) {
		this.product_brand = product_brand;
	}
	/**
	 * @return the product_name
	 */
	public String getProduct_name() {
		return product_name;
	}
	/**
	 * @param product_name the product_name to set
	 */
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	/**
	 * @return the old_price
	 */
	public String getOld_price() {
		return old_price;
	}
	/**
	 * @param old_price the old_price to set
	 */
	public void setOld_price(String old_price) {
		this.old_price = old_price;
	}
	/**
	 * @return the new_price
	 */
	public String getNew_price() {
		return new_price;
	}
	/**
	 * @param new_price the new_price to set
	 */
	public void setNew_price(String new_price) {
		this.new_price = new_price;
	}
	/**
	 * @return the product_info
	 */
	public String getProduct_info() {
		return product_info;
	}
	/**
	 * @param product_info the product_info to set
	 */
	public void setProduct_info(String product_info) {
		this.product_info = product_info;
	}
	/**
	 * @return the product_img
	 */
	public String getProduct_img() {
		return product_img;
	}
	/**
	 * @param product_img the product_img to set
	 */
	public void setProduct_img(String product_img) {
		this.product_img = product_img;
	}
	
}
